var num = 1634;
var sum = 0;
var power = 4;
num = num.toString().split('')

function Narcissistic(num, power) {

  for (i = 0; i < num.length; i++) {

    sum = sum + Math.pow(num[i], power)
  }
  num = num.join('')
  return num == sum ? true : false
}

Narcissistic(num, power);

